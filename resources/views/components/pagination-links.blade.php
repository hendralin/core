// Function to generate pagination elements with three dots logic
function generatePaginationElements($paginator) {
    $currentPage = $paginator->currentPage();
    $lastPage = $paginator->lastPage();
    $elements = [];

    if ($lastPage <= 5) {
        // If total pages are 5 or less, show all pages
        for ($i = 1; $i <= $lastPage; $i++) {
            $elements[] = [$i => null];
        }
    } else {
        // Complex logic for more than 5 pages
        if ($currentPage <= 3) {
            // Show first 3 pages, dots, and last page
            for ($i = 1; $i <= 3; $i++) {
                $elements[] = [$i => null];
            }
            if ($lastPage > 4) {
                $elements[] = '...';
                $elements[] = [$lastPage => null];
            }
        } elseif ($currentPage >= $lastPage - 2) {
            // Show first page, dots, and last 3 pages
            $elements[] = [1 => null];
            if ($lastPage > 4) {
                $elements[] = '...';
            }
            for ($i = $lastPage - 2; $i <= $lastPage; $i++) {
                $elements[] = [$i => null];
            }
        } else {
            // Show first page, dots, current page with neighbors, dots, last page
            $elements[] = [1 => null];
            $elements[] = '...';

            for ($i = $currentPage - 1; $i <= $currentPage + 1; $i++) {
                $elements[] = [$i => null];
            }

            $elements[] = '...';
            $elements[] = [$lastPage => null];
        }
    }

    return $elements;
}

$elements = generatePaginationElements($paginator);
